/* 
 *   bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
int bitXor(int x, int y) {
  // applying Demorgan's Law to (~x&y) || (x & ~y)
  return ~(~(~x & y) & ~(x & ~y));
}
/* 
 * bitOr - x|y using only ~ and & 
 *   Example: bitOr(6, 5) = 7
 *   Legal ops: ~ &
 *   Max ops: 8
 *   Rating: 1
 */
int bitOr(int x, int y) {
    // applying Demorgan's Law to (x | y)
    return ~(~x & ~y);
}
