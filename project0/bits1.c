    int mask4 = mask3 ^ ((mask3) << 2); //mask4
    int mask5 = mask4 ^ (mask4 << 1); // mask5
    x = x << 16 | ((x >> 16) & mask1);
    x = (x & mask2) << 8 | ((x >> 8) & mask2);
    x = (x & mask3) << 4 | ((x >> 4) & mask3);
