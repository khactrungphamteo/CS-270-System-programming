/* 
 *  addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
  int sum = x + y;
  return !(((sum ^ x) & (sum ^ y)) >> 31);
}
/* 
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 */
int replaceByte(int x, int n, int c) {
  int mask, shift;
  mask = 255; // byte-wide mask of 1s
  shift = n << 3; // == n*2^3 == n * 8
  mask = ~(mask << shift); // shift byte-wide mask, flip to get byte-wide mask of 0s
  c = c << shift; // shift bits of c into nth byte
  return (x & mask) | c; // apply mask to x, zeroing x's nth byte, replacing it with byte given by c 
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
  // if x is anything but 0, subtract 1 from
  int mask = (!x + ~0x00);
  return ((~mask) & z) | ((mask) & y);
  
}

/* 
 *   bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
  // logical negative value of x
  // 2's compliment of x
  // if (x != 0) the most significant bit of either x or -x is 1
  return (((~x + 1) | x) >> 31) + 1; 
}


